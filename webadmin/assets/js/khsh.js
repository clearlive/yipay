	function ck(obj){//查看
		$.ajax({
			url:'',
			data:{
				id:$(obj).attr('val'),
				action:'chakan',
			},
			dataType:'JSON',
			type:'POST',
			success:function(data){
				if(data.shlx=='gr'){
					data.shlx='个人';
				}else{
					data.shlx='企业';
				}
				$('#shmc').html(data.shmc);	
				$('#shmm').html(data.shmm);	
				$('#shsj').html(data.shsj);	
				$('#shyx').html(data.shyx);	
				$('#lxqq').html(data.lxqq);	
				$('#zsxm').html(data.zsxm);	
				$('#shxb').html(data.shxb);	
				$('#sfhm').html(data.sfhm);	
				$('#hjdz').html(data.hjdz);	
				$('#sfdz').html(data.sfdz);	
				$('#sfzm').attr("src",data.sfzm);	
				$('#sffm').attr("src",data.sffm);	
				$('#shlx').html(data.shlx);	
				$('#wzmc').html(data.wzmc);	
				$('#wzwz').html(data.wzwz);	
				$('#wzlx').html(data.wzlx);	
				$('#txyh').html(data.txyh);	
				$('#txzh').html(data.txzh);	
				$('#sj').html(data.sj);	
				$('#xq').modal();
			},
			error:function(){
				$('#ts').modal();
				$('#te').html("获取数据错误");
			},
		})
	}
	
	
	function tg(obj){//通过
		$('#tgmodal').modal({
			onConfirm: function() {//邮件通知
				$.ajax({
					url:'',
					data:{
						action:'tongguo',
						id:$(obj).attr('val'),
						mail:'yes',
					},
					type:'POST',
					dataType:'JSON',
					success:function(data){
						$('#ts').modal();
						$('#te').html(data.te);
						if(data.ok){
							setTimeout(function(){
								window.location.reload();//页面刷新
							},1000);
						}
					},
					error:function(){
						$('#ts').modal();
						$('#te').html("获取数据错误");
					}
				})
			},
			onCancel: function() {   //不邮件通知
				$.ajax({
					url:'',
					data:{
						action:'tongguo',
						id:$(obj).attr('val'),
					},
					type:'POST',
					dataType:'JSON',
					success:function(data){
						$('#ts').modal();
						$('#te').html(data.te);
						if(data.ok){
							setTimeout(function(){
								window.location.reload();//页面刷新
							},1000);
						}
					},
					error:function(){
						$('#ts').modal();
						$('#te').html("获取数据错误");
					}
				})
			}
		})
	}
		
	
